namespace BP.ColourChimp.Classes
{
    /// <summary>
    /// Enumeration of color spaces.
    /// </summary>
    public enum ColorSpace
    {
        /// <summary>
        /// Alpha, red, green, blue color space.
        /// </summary>
        ARGB = 0,
        /// <summary>
        /// Cyan, magenta, yellow, key color space.
        /// </summary>
        CMYK,
        /// <summary>
        /// Hue, saturation, value color space.
        /// </summary>
        HSV
    }
}